# 整型
int_1 = 666
int_2 = 666666666

# 浮点型
float_1 = 8.88
float_2 = -5.55

# 字符串
name = 'yhf'

# 列表：有序的，可变
list_1 = ['yhf', 'ykx', 3]
list_1.append('add_value')
print(list_1[-1])

# 字典
dict_1 = {'ykx': 'yukexin', 'yhf': 'yuhuifei', 'age': 35}
dict_2 = dict(ykx='yukexin', yhf='yuhuifei', age=35)

# 集合:无序，没有重复的元素
set_1 = {'yhf', 'ykx'}

# 元组：有序的，不可变
tuple_1 = (1, 2, 'ykx', 'yhf')

# 布尔
bool_1 = True
bool_2 = False
